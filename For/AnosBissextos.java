public class AnosBissextos {

	public static void main (String[] args ) {
	
		int i;
		
		for(i=0;i!=2023;i++) {
		
			if(i%4==0) {
				System.out.println(i+" é ano bissexto");			
               }
			else {
			System.out.println(i+" é um ano normal");
			}		
			
			
}
}
}